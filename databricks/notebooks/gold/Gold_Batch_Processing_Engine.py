    "def build_regulatory_view():\n",
    "    \"\"\"\n",
    "    G2: Aggregate fact for OSFI B-10 compliance, gross/net face amounts and premiums\n",
    "    by province and product.\n",
    "    \"\"\"\n",
    "    policy_enriched_df = read_silver_source(\"policy.individual_life_enriched\")\n",
    "    reinsurance_df = read_silver_source(\"reinsurance.treaty_clean\")\n",
    "    \n",
    "    # Policy aggregates using the live compact canonical status values.\n",
    "    policy_current_df = policy_enriched_df.withColumn(\n",
    "        \"policy_status_canonical_normalized\",\n",
    "        F.upper(F.trim(F.coalesce(F.col(\"policy_status_canonical\"), F.lit(\"\"))))\n",
    "    ).filter(\n",
    "        ~F.col(\"policy_status_canonical_normalized\").isin(\"L\", \"C\")\n",
    "    )\n",
    "\n",
    "    policy_agg_df = policy_current_df.groupBy(\n",
    "        F.col(\"product_type_code_canonical\"),\n",
    "        F.col(\"province_clean\"),\n",
    "        F.col(\"policy_status_canonical\"),\n",
    "    ).agg(\n",
    "        F.count(\"*\").alias(\"policy_count\"),\n",
    "        F.sum(\"face_amount\").alias(\"gross_face_amount\"),\n",
    "        F.sum(\"annualised_premium\").alias(\"gross_annualised_premium\"),\n",
    "        F.sum(\n",
    "            F.when(F.col(\"issue_date\") >= F.lit(\"2023-01-01\"), F.col(\"annualised_premium\")).otherwise(0)\n",
    "        ).alias(\"ifrs17_premium\"),\n",
    "    )\n",
    "    \n",
    "    # Reinsurance ceded amounts (join on policy_number, aggregate to same grain)\n",
    "    reinsurance_agg_df = reinsurance_df.groupBy(\n",
    "        F.col(\"policy_number\"),\n",
    "        F.col(\"product_type_code\"),\n",
    "        F.col(\"policy_status_code\"),\n",
    "    ).agg(\n",
    "        F.sum(\"ceded_amount\").alias(\"ceded_face_amount\"),\n",
    "        F.sum(\"premium_ceded\").alias(\"ceded_premium\"),\n",
    "    )\n",
    "    \n",
    "    # Join policy and reinsurance at policy level, then re-aggregate.\n",
    "    # Keep this on the same current-state life slice used in policy_agg_df.\n",
    "    policy_reinsurance_df = policy_current_df.join(\n",
    "        reinsurance_agg_df,\n",
    "        (policy_current_df[\"policy_number\"] == reinsurance_agg_df[\"policy_number\"])\n",
    "        & (policy_current_df[\"product_type_code\"] == reinsurance_agg_df[\"product_type_code\"]),\n",
    "        \"left\"\n",
    "    ).groupBy(\n",
    "        F.col(\"product_type_code_canonical\"),\n",
    "        F.col(\"province_clean\"),\n",
    "        F.col(\"policy_status_canonical\"),\n",
    "    ).agg(\n",
    "        F.sum(\"ceded_face_amount\").alias(\"total_ceded_face_amount\"),\n",
    "        F.sum(\"ceded_premium\").alias(\"total_ceded_premium\"),\n",
    "    )\n",
    "    \n",
    "    # Merge policy aggregates with ceded amounts\n",
    "    regulatory_df = policy_agg_df.join(\n",
    "        policy_reinsurance_df,\n",
    "        [\"product_type_code_canonical\", \"province_clean\", \"policy_status_canonical\"],\n",
    "        \"left\"\n",
    "    ).withColumn(\n",
    "        \"net_face_amount\",\n",
    "        F.col(\"gross_face_amount\") - F.coalesce(F.col(\"total_ceded_face_amount\"), F.lit(0))\n",
    "    ).withColumn(\n",
    "        \"net_annualised_premium\",\n",
    "        F.col(\"gross_annualised_premium\") - F.coalesce(F.col(\"total_ceded_premium\"), F.lit(0))\n",
    "    )\n",
    "    \n",
    "    # Add gold metadata\n",
    "    result_df = regulatory_df.withColumn(\n",
    "        \"gold_ingested_at\", F.current_timestamp()\n",
    "    ).withColumn(\n",
    "        \"gold_run_id\", F.lit(run_id)\n",
    "    )\n",
    "    \n",
    "    return result_df\n",
