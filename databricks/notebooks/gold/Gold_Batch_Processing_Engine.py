    "def build_ml_features():\n",
    "    \"\"\"\n",
    "    G3: Feature store with full SCD2 history for lapse_count_lifetime, reinstatement_count,\n",
    "    no PII, c360_customer_id only.\n",
    "    \"\"\"\n",
    "    policy_clean_df = read_silver_source(\"policy.individual_life_clean\")\n",
    "    digital_df = read_silver_source(\"digital.portal_clean\")\n",
    "    retirement_df = read_silver_source(\"group_retirement.member_clean\")\n",
    "    \n",
    "    # Policy features: use full SCD2 history (NO is_current filter), normalize status codes\n",
    "    # before lapse/reinstatement pattern detection.\n",
    "    policy_with_normalized_status = policy_clean_df.withColumn(\n",
    "        \"policy_status_normalized\",\n",
    "        F.upper(F.trim(F.coalesce(F.col(\"policy_status_code\"), F.lit(\"\"))))\n",
    "    )\n",
    "\n",
    "    policy_features_df = policy_with_normalized_status.groupBy(\"customer_id\").agg(\n",
    "        F.count(\"*\").alias(\"total_policy_history_count\"),\n",
    "        F.sum(\n",
    "            F.when(F.col(\"policy_status_normalized\").isin(\"L\", \"LAPSED\"), 1).otherwise(0)\n",
    "        ).alias(\"lapse_count_lifetime\"),\n",
    "        F.sum(\n",
    "            F.when(F.col(\"policy_status_normalized\").isin(\"R\", \"REINSTATED\"), 1).otherwise(0)\n",
    "        ).alias(\"reinstatement_count\"),\n",
    "        F.min(\"issue_date\").alias(\"first_policy_issue_date\"),\n",
    "    )\n",
    "    \n",
    "    # Digital engagement score (computed from event_type counts, no PII)\n",
    "    digital_features_df = digital_df.groupBy(\"customer_id\").agg(\n",
    "        F.sum(\n",
    "            F.when(F.col(\"event_type\") == \"login\", 1).otherwise(0)\n",
    "        ).alias(\"logins_count\"),\n",
    "        F.sum(\n",
    "            F.when(F.col(\"event_type\") == \"doc_download\", 1).otherwise(0)\n",
    "        ).alias(\"doc_downloads_count\"),\n",
    "        F.max(\"event_timestamp\").alias(\"last_digital_event_ts\"),\n",
    "    ).withColumn(\n",
    "        \"digital_engagement_score\",\n",
    "        (F.col(\"logins_count\") * 1.0) + (F.col(\"doc_downloads_count\") * 2.0)\n",
    "    )\n",
    "    \n",
    "    # Retirement participation (via member_id = customer_id), normalize status before active filter.\n",
    "    retirement_features_df = retirement_df.withColumn(\n",
    "        \"member_status_normalized\",\n",
    "        F.upper(F.trim(F.coalesce(F.col(\"member_status\"), F.lit(\"\"))))\n",
    "    ).filter(\n",
    "        F.col(\"member_status_normalized\") == \"ACTIVE\"\n",
    "    ).groupBy(\n",
    "        F.col(\"member_id\").alias(\"customer_id\")\n",
    "    ).agg(\n",
    "        F.sum(\"contribution_amount\").alias(\"total_retirement_contribution\"),\n",
    "        F.when(\n",
    "            F.sum(\"contribution_amount\") > 0, F.lit(True)\n",
    "        ).otherwise(F.lit(False)).alias(\"has_retirement_account\"),\n",
    "    )\n",
    "    \n",
    "    # Combine features\n",
    "    ml_features_df = policy_features_df.join(\n",
    "        digital_features_df, [\"customer_id\"], \"left\"\n",
    "    ).join(\n",
    "        retirement_features_df, [\"customer_id\"], \"left\"\n",
    "    )\n",
    "    \n",
    "    # Rename customer_id to c360_customer_id for ML context\n",
    "    result_df = ml_features_df.withColumnRenamed(\n",
    "        \"customer_id\", \"c360_customer_id\"\n",
    "    ).withColumn(\n",
    "        \"gold_ingested_at\", F.current_timestamp()\n",
    "    ).withColumn(\n",
    "        \"gold_run_id\", F.lit(run_id)\n",
    "    )\n",
    "    \n",
    "    return result_df\n",
