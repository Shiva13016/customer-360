    "def build_kpi_summary():\n",
    "    \"\"\"\n",
    "    G5: Daily KPIs aggregate including 30d churn rate using SCD2 effective_date.\n",
    "    \"\"\"\n",
    "    policy_clean_df = read_silver_source(\"policy.individual_life_clean\")\n",
    "    policy_with_normalized_status = policy_clean_df.withColumn(\n",
    "        \"policy_status_normalized\",\n",
    "        F.upper(F.trim(F.coalesce(F.col(\"policy_status_code\"), F.lit(\"\"))))\n",
    "    )\n",
    "    \n",
    "    # Calculate 30-day churn rate using SCD2 effective_date and live status values.\n",
    "    churn_window_df = policy_with_normalized_status.filter(\n",
    "        F.col(\"effective_date\").isNotNull()\n",
    "    ).filter(\n",
    "        F.datediff(F.current_date(), F.col(\"effective_date\")).between(0, 30)\n",
    "    ).groupBy(\n",
    "        F.to_date(F.col(\"effective_date\")).alias(\"kpi_date\")\n",
    "    ).agg(\n",
    "        F.countDistinct(\"customer_id\").alias(\"active_customers\"),\n",
    "        F.sum(\n",
    "            F.when(F.col(\"policy_status_normalized\").isin(\"L\", \"LAPSED\", \"C\", \"CANCELLED\"), 1).otherwise(0)\n",
    "        ).alias(\"churned_customers\"),\n",
    "    ).withColumn(\n",
    "        \"churn_rate_30d\",\n",
    "        F.when(\n",
    "            F.col(\"active_customers\") > 0,\n",
    "            F.col(\"churned_customers\") / F.col(\"active_customers\")\n",
    "        ).otherwise(F.lit(0.0))\n",
    "    )\n",
    "    \n",
    "    # Other daily KPIs (policies issued, premiums written) on the live current-state slice.\n",
    "    daily_kpis_df = policy_with_normalized_status.filter(\n",
    "        F.col(\"policy_status_normalized\").isin(\"ACTIVE\", \"PENDING\", \"SUSPENDED\", \"A\", \"P\", \"S\")\n",
    "    ).filter(\n",
    "        F.col(\"issue_date\").isNotNull()\n",
    "    ).groupBy(\n",
    "        F.to_date(F.col(\"issue_date\")).alias(\"kpi_date\")\n",
    "    ).agg(\n",
    "        F.count(\"*\").alias(\"policies_issued\"),\n",
    "        F.sum(\"premium_amount\").alias(\"total_premium_written\"),\n",
    "        F.sum(\"face_amount\").alias(\"total_face_amount_issued\"),\n",
    "    )\n",
    "    \n",
    "    # Merge KPIs\n",
    "    kpi_df = churn_window_df.join(\n",
    "        daily_kpis_df, [\"kpi_date\"], \"outer\"\n",
    "    )\n",
    "    \n",
    "    # Add gold metadata\n",
    "    result_df = kpi_df.withColumn(\n",
    "        \"gold_ingested_at\", F.current_timestamp()\n",
    "    ).withColumn(\n",
    "        \"gold_run_id\", F.lit(run_id)\n",
    "    )\n",
    "    \n",
    "    return result_df\n",
