    "# G2: regulatory_view - Aggregate fact for OSFI B-10 compliance  \n",
    "# G3: ml_features - Feature store with full SCD2 history, no PII\n",
    "# G4: book_of_business - Fact with Unity Catalog RLS row filter on advisor_id\n",
    "# G5: kpi_summary - Daily KPIs aggregate\n",
    "# ------------------------------------------------------------------------------\n",
    "def build_customer_360():\n",
    "    \"\"\"\n",
    "    G1: Wide denormalised fact with 1 row/customer, current state, multi-source LEFT JOINs,\n",
    "    cross-sell propensity signals.\n",
    "    \"\"\"\n",
    "    customer_df = read_silver_source(\"customer.master\")\n",
    "    policy_enriched_df = read_silver_source(\"policy.individual_life_enriched\")\n",
    "    disability_df = read_silver_source(\"policy.disability_ci_clean\")\n",
    "    group_benefits_df = read_silver_source(\"group_benefits.certificate_coverage_detail\")\n",
    "    group_retirement_df = read_silver_source(\"group_retirement.member_clean\")\n",
    "    investments_df = read_silver_source(\"investments.climl_clean\")\n",
    "    callcentre_df = read_silver_source(\"interactions.callcentre_clean\")\n",
    "    \n",
    "    # Base: customer master\n",
    "    base_df = customer_df.select(\n",
    "        F.col(\"customer_id\"),\n",
    "        F.col(\"first_name\"),\n",
    "        F.col(\"last_name\"),\n",
    "        F.col(\"email_clean\"),\n",
    "        F.col(\"phone_clean\"),\n",
    "        F.col(\"province_clean\"),\n",
    "        F.col(\"postal_code_clean\"),\n",
    "        F.col(\"channel\"),\n",
    "        F.col(\"advisor_id\"),\n",
    "    )\n",
    "    \n",
    "    # Aggregate policy metrics using the live silver contract.\n",
    "    # `policy_status_canonical` is stored as compact codes (A, C, S, L, P), not words.\n",
    "    # Exclude terminal life statuses so current coverage metrics do not count lapsed/cancelled policies.\n",
    "    policy_current_df = policy_enriched_df.withColumn(\n",
    "        \"policy_status_canonical_normalized\",\n",
    "        F.upper(F.trim(F.coalesce(F.col(\"policy_status_canonical\"), F.lit(\"\"))))\n",
    "    ).filter(\n",
    "        ~F.col(\"policy_status_canonical_normalized\").isin(\"L\", \"C\")\n",
    "    )\n",
    "\n",
    "    policy_agg_df = policy_current_df.groupBy(\"customer_id\").agg(\n",
    "        F.count(\"*\").alias(\"life_policy_count\"),\n",
    "        F.sum(\"face_amount\").alias(\"total_life_face_amount\"),\n",
    "        F.sum(\"annualised_premium\").alias(\"total_life_premium\"),\n",
    "        F.max(F.col(\"term_expiring_90d_flag\").cast(\"int\")).alias(\"has_term_expiring_90d\"),\n",
    "        F.min(\"issue_date\").alias(\"first_policy_date\"),\n",
    "    )\n",
    "    \n",
    "    # Disability/CI coverage flags using current non-terminal records from the live silver contract.\n",
    "    disability_current_df = disability_df.withColumn(\n",
    "        \"policy_status_code_normalized\",\n",
    "        F.upper(F.trim(F.coalesce(F.col(\"policy_status_code\"), F.lit(\"\"))))\n",
    "    ).filter(\n",
    "        ~F.col(\"policy_status_code_normalized\").isin(\"LAPSED\", \"CANCELLED\")\n",
    "    )\n",
    "\n",
    "    disability_agg_df = disability_current_df.groupBy(\"customer_id\").agg(\n",
    "        F.when(\n",
    "            F.count(\"*\") > 0, F.lit(True)\n",
    "        ).otherwise(F.lit(False)).alias(\"has_disability_coverage\"),\n",
    "        F.when(\n",
    "            F.sum(F.when(F.col(\"product_type_code\").rlike(\"(?i)CI|CRITICAL\"), 1).otherwise(0)) > 0,\n",
    "            F.lit(True)\n",
    "        ).otherwise(F.lit(False)).alias(\"has_ci_coverage\"),\n",
    "    )\n",
    "    \n",
    "    # Group benefits: active members only\n",
    "    group_benefits_agg_df = group_benefits_df.filter(\n",
    "        F.col(\"termination_date\").isNull()\n",
    "    ).groupBy(\n",
    "        F.col(\"member_id\").alias(\"customer_id\")\n",
    "    ).agg(\n",
